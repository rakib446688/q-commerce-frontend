import fs from "node:fs/promises";
import { getCatalogPaths, readJson, writeJson } from "./catalog.mjs";

function escapeSqlString(value) {
  return String(value).replace(/'/g, "''");
}

function sqlText(value) {
  return `'${escapeSqlString(value)}'`;
}

function sqlTextArray(values) {
  if (!values || values.length === 0) return "array[]::text[]";
  const items = values.map((v) => sqlText(v)).join(",");
  return `array[${items}]`;
}

function sqlJsonbObject(obj) {
  const json = JSON.stringify(obj || {});
  return `'${escapeSqlString(json)}'::jsonb`;
}

function assertPriceMap(prices, slugs) {
  const missing = [];
  const invalid = [];

  for (const slug of slugs) {
    const value = prices?.[slug];
    if (value === null || value === undefined) {
      missing.push(slug);
      continue;
    }
    const num = Number(value);
    if (!Number.isFinite(num) || num < 0) invalid.push(slug);
  }

  if (missing.length) {
    throw new Error(
      `Missing prices for ${missing.length} products. Fill them in your prices file first (e.g. data/prices.json). First missing: ${missing[0]}`
    );
  }
  if (invalid.length) throw new Error(`Invalid prices for ${invalid.length} products. First invalid: ${invalid[0]}`);
}

async function main() {
  const paths = getCatalogPaths();

  const args = new Map();
  for (let i = 2; i < process.argv.length; i += 1) {
    const part = process.argv[i];
    const next = process.argv[i + 1];
    if (part?.startsWith("--")) args.set(part.slice(2), next && !next.startsWith("--") ? next : "true");
  }

  const productsPath = args.get("products") || "data/catalog.products.with-images.json";
  const pricesPath = args.get("prices") || "data/prices.json";

  const categoriesCfg = await readJson(paths.categories);
  const products = await readJson(productsPath);

  const pricesExists = await fs
    .stat(pricesPath)
    .then(() => true)
    .catch(() => false);

  if (!pricesExists) {
    const template = await readJson(paths.pricesTemplate);
    await writeJson(pricesPath, template);
    throw new Error(`Created ${pricesPath}. Fill in prices, then rerun this script.`);
  }

  const prices = await readJson(pricesPath);
  assertPriceMap(prices, products.map((p) => p.slug));

  const categoryValues = categoriesCfg.categories
    .map((c) => `(${sqlText(c.name)}, ${sqlText(c.slug)})`)
    .join(",\n  ");

  const productValues = products
    .map((p) => {
      const categorySlug = p.category_slug || (p.category === "Home & Living" ? "home-living" : p.category.toLowerCase());
      const price = Number(prices[p.slug]).toFixed(2);
      const categoryIdSql = `(select id from public.categories where slug = ${sqlText(categorySlug)})`;

      return `(
  ${sqlText(p.slug)},
  ${sqlText(p.title)},
  ${sqlText(p.category)},
  ${p.subcategory ? sqlText(p.subcategory) : "null"},
  ${categoryIdSql},
  ${price},
  ${Number(p.rating_avg || 0).toFixed(2)},
  ${Number(p.rating_count || 0) | 0},
  ${sqlTextArray(p.colors || [])},
  ${sqlTextArray(p.sizes || [])},
  ${sqlJsonbObject(p.color_images || {})},
  ${p.brand ? sqlText(p.brand) : "null"},
  true,
  ${Number(p.stock_qty || 0) | 0}
)`;
    })
    .join(",\n\n");

  const sql = `-- Generated by scripts/build-supabase-products-sql.mjs
-- Catalog images are local paths under /public/products.

begin;

-- Replace catalog (non-destructive): keep old rows but hide them
update public.products set is_active = false;

insert into public.categories (name, slug)
values
  ${categoryValues}
on conflict (slug) do update
set
  name = excluded.name,
  updated_at = now();

insert into public.products (
  slug,
  title,
  category,
  subcategory,
  category_id,
  price,
  rating_avg,
  rating_count,
  colors,
  sizes,
  color_images,
  brand,
  is_active,
  stock_qty
)
values
${productValues}
on conflict (slug) do update
set
  title = excluded.title,
  category = excluded.category,
  subcategory = excluded.subcategory,
  category_id = excluded.category_id,
  price = excluded.price,
  rating_avg = excluded.rating_avg,
  rating_count = excluded.rating_count,
  colors = excluded.colors,
  sizes = excluded.sizes,
  color_images = excluded.color_images,
  brand = excluded.brand,
  is_active = true,
  stock_qty = excluded.stock_qty,
  updated_at = now();

commit;
`;

  const outPath = "supabase_seed_products_catalog.sql";
  await fs.writeFile(outPath, sql, "utf8");
  console.log(`Wrote ${outPath}`);
}

main().catch((err) => {
  console.error(err.message || err);
  process.exitCode = 1;
});

